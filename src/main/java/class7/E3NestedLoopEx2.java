package class7;

public class E3NestedLoopEx2 {
    public static void main(String[] args) {



        for (int j = 0; j <3 ; j++) {// how many line it will print
            for (int i = 2; i < 20; i=i+2) {//all the even numbers
                System.out.print(i+ " ");

            }
            System.out.println();// this is to give space to print

        }

    }
}
