package projectCodes1;
// to swap the value of string a ans string b
public class SwapVar {
    public static void main(String[] args) {

        String a="Apple";
        String b="Cherry";

        String temp;

        temp=a;
        a=b;
        b=temp;

        System.out.println(a);
        System.out.println(b);

    }
}
