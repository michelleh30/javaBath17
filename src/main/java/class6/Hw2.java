package class6;
//Print even numbers from 20 to 1 (using 2 different loops)
public class Hw2 {
    public static void main(String[] args) {

        int i=20;//starting point
        while(i>=1) {//ending point
            System.out.print(i+ " ");//space after each output " "
            i-=2;


        }
    }
}
