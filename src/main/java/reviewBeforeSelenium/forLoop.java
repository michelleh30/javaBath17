package reviewBeforeSelenium;
//print this 5 timea
public class forLoop {
    public static void main(String[] args) {

        for (int i = 0; i <4 ; i++) {//how many times do we want to repeat the code 10 x
            System.out.println("I love dogs");

        }
        for (int i = 0; i <15 ; i++) {
            System.out.println(i);//this print out i, which are numbers

        }
        int [] grades = {98,100,67,88,98};
        for (int i = 0; i <grades.length ; i++) {
            System.out.println(grades[i]);

        }



    }
}
