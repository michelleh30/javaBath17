package class4;
// If else statements: If else statement helps us write code that can take decision based on a
// value and can execute selective code. they work based on a boolean value.
public class E1IfElseStatements {
    public static void main(String[] args) {

        int age=10;
        boolean condition=age>5;
        if(condition){
            System.out.println(1);

        }else {
            System.out.println(2);
        }

    }
}
